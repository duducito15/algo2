package Poo;
/*
POO (Programacion Orientada a Objetos), es un paradigma de programacion, la clave es que todo
es un objeto que toman el nombre de instancia, un objeto tiene caracteristicas se conocen
como atributos o variables de la instancia.

Clase: es una abstracion que denota cierta cualidades
Instancia: objeto palpable que deriva de una clase
Atributos: conjunto de caracteristicas que comparten los objetos de la clase

*/

public class Libro {
    //Atributos de la clase
    String isbn;
    String titulo;
    String autor;
    String portada;
    int numeroPaginas;
}


