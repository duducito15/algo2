package Funciones.Librerias;

public class Basicas {

    //Calcula la suma de dos numeros enteros
    public static int miSuma(int x, int y){
        return x + y;
    }
    //Calcula la resta de dos numeros enteros
    public static int miResta(int x, int y){
        return x - y;
    }
    //Calcula la multiplicacion de dos numeros enteros
    public static int miMulti(int x, int y){
        return x * y;
    }
        
    //Calcula la division de dos numeros enteros
    public static double miDivision(int x, int y){
        return (double)x / (double)y;
    }
}
