package Funciones.Librerias;

public class Geometria {
    // Crear funciones que me permitan hacer operaciones sobre una
    // circunferencia, puede Area, Perimetro y otras
    public static double areaCirculo(double r) {
        return Math.PI * r * r;
    }

    public static double perimetroCirculo(double r) {
        return Math.PI * r * 2;
    }

}
